from RPLCD.i2c import CharLCD
import time

lcd = CharLCD(i2c_expander='PCF8574', address=0x26, port=1, cols=16, rows=2, dotsize=8)
lcd.clear()

font = [
  [0x00, 0x00, 0x03, 0x04, 0x08, 0x19, 0x11, 0x10],
  [0x00, 0x1F, 0x00, 0x00, 0x11, 0x11, 0x11, 0x00],
  [0x00, 0x00, 0x18, 0x04, 0x02, 0x13, 0x11, 0x01],
  [0x12, 0x13, 0x1B, 0x09, 0x04, 0x03, 0x00, 0x00],
  [0x00, 0x11, 0x1f, 0x1f, 0x0e, 0x00, 0x1f, 0x00],
  [0x09, 0x19, 0x1b, 0x12, 0x04, 0x18, 0x00, 0x00]
]
i = 0
for char in font:
  lcd.create_char(i, char)
  i += 1

lcd.clear()
lcd.home()

lcd.write(0)
lcd.write(1)
lcd.write(2)
lcd.crlf()
lcd.write(3)
lcd.write(4)
lcd.write(5)